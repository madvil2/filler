#include "libft.h"

char		*ft_itoa(int n)
{
	char	*str;
	int		len;

	if (n < -2147483647)
		return (ft_strdup("-2147483648"));
	len = ft_itoalen(n);
	str = (char*)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	if (n < 0)
	{
		str[0] = '-';
		n = n * -1;
	}
	if (n == 0)
		str[0] = '0';
	str[len] = '\0';
	len--;
	while (n)
	{
		str[len] = (n % 10) + '0';
		n = n / 10;
		len--;
	}
	return (str);
}
