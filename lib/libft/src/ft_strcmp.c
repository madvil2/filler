#include "libft.h"

int					ft_strcmp(const char *s1, const char *s2)
{
	int				i;
	unsigned char	*us1;
	unsigned char	*us2;

	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	i = 0;
	while (us1[i] == us2[i] && us1[i] != '\0' && us2[i] != '\0')
		i++;
	return (us1[i] - us2[i]);
}
