#include "libft.h"

void				*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t			i;
	unsigned char	*s1;
	unsigned char	*s2;

	i = 0;
	s1 = (unsigned char *)dst;
	s2 = (unsigned char *)src;
	if (s1 < s2)
		while (i < len)
		{
			s1[i] = s2[i];
			i++;
		}
	else
	{
		i = len - 1;
		while (((int)i) >= 0)
		{
			s1[i] = s2[i];
			i--;
		}
	}
	return (dst);
}
